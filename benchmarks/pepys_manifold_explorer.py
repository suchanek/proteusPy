# DiaryTransformer ingestion (only needed when rebuilding cache)
# ---------------------------------------------------------------------------
def ingest_diary(
    diary_path: str, max_chunks_per_entry: int = 3
) -> tuple[list[str], list[datetime]]:
    """Parse and enrich a pipe-delimited diary via DiaryTransformer.

    Produces one text string per chunk: ``semantic_category | content``.
    Each diary entry becomes 1–3 chunks at sentence boundaries.

    Requires diary_kg on sys.path (``pip install -e ../diary_kg`` or pass
    ``--diary-kg-src`` to prepend the path automatically).

    :param diary_path: Path to the pipe-delimited diary source file.
    :param max_chunks_per_entry: Max chunks per entry (0 = unlimited).
    :return: Tuple of (embed strings, datetime objects).
    """
    from diary_transformer.parser import parse_diary_file
    from diary_transformer.transformer import DiaryTransformer

    console.print(f"  Parsing {diary_path} …")
    entries = parse_diary_file(diary_path)
    console.print(f"  Parsed {len(entries)} diary entries.")

    dt = DiaryTransformer(chunking_strategy="sentence_group", sentences_per_chunk=4)
    chunks = dt.transform_entries(entries, max_chunks_per_entry=max_chunks_per_entry or 999)

    texts: list[str] = []
    timestamps: list[datetime] = []
    for c in chunks:
        label = (c.semantic_category or "").strip()
        content = (c.content or "").strip()
        if not content:
            continue
        texts.append(f"{label} | {content}" if label else content)
        try:
            ts = (
                datetime.fromisoformat(c.timestamp)
                if isinstance(c.timestamp, str)
                else c.timestamp
            )
        except (ValueError, TypeError):
            continue
        timestamps.append(ts)

    console.print(
        f"  Produced {len(texts)} chunks from {len(entries)} entries  "
        f"({timestamps[0].date()} → {timestamps[-1].date()})"
    )
    return texts, timestamps


# ---------------------------------------------------------------------------
# Embedding — mpnet, NO task prefix
# ---------------------------------------------------------------------------
def _embed_shard(args: tuple) -> np.ndarray:
    """Embed one shard in a subprocess (mpnet, no prefix).

    :param args: (texts_shard, model_id, batch_size, worker_id)
    :return: Float32 array of shape (len(shard), D).
    """
    texts_shard, model_id, batch_size, worker_id = args
    embedder = SentenceTransformer(model_id, trust_remote_code=False)
    vecs = embedder.encode(
        texts_shard,
        batch_size=batch_size,
        convert_to_numpy=True,
        show_progress_bar=(worker_id == 0),
    )
    return vecs.astype(np.float32)


def embed_corpus(
    texts: list[str],
    model: str = EMBED_MODEL,
    n_workers: int | None = None,
    batch_size: int = 64,
) -> np.ndarray:
    """Embed texts with all-mpnet-base-v2 using multiprocessing.Pool.

    No task prefix is added — mpnet embeds raw text directly.

    :param texts: Strings to embed.
    :param model: HuggingFace model id.
    :param n_workers: Parallel workers (default: os.cpu_count()).
    :param batch_size: Batch size per worker.
    :return: Float32 (N, D) array.
    """
    import os

    n_workers = n_workers or os.cpu_count() or 1
    n_workers = min(n_workers, len(texts))
    chunk_size = (len(texts) + n_workers - 1) // n_workers
    shards = [
        texts[i * chunk_size : (i + 1) * chunk_size]
        for i in range(n_workers)
        if texts[i * chunk_size : (i + 1) * chunk_size]
    ]
    pool_args = [(shard, model, batch_size, idx) for idx, shard in enumerate(shards)]

    console.print(f"  Spawning {len(shards)} workers × {chunk_size} entries …")
    with multiprocessing.Pool(len(shards)) as pool:
        results = pool.map(_embed_shard, pool_args)

    return np.concatenate(results, axis=0)


def embed_queries(
    texts: list[str],
    model: str = EMBED_MODEL,
    batch_size: int = 64,
) -> np.ndarray:
    """Single-process query embedder — no task prefix.

    :param texts: Query strings.
    :param model: HuggingFace model id.
    :param batch_size: Batch size.
    :return: Float32 (N, D) array.
    """
    embedder = SentenceTransformer(model, trust_remote_code=False)
    vecs = embedder.encode(
        texts,
        batch_size=batch_size,
        convert_to_numpy=True,
        show_progress_bar=False,
    )
    return vecs.astype(np.float32)


# ---------------------------------------------------------------------------
# Cache I/O
# ---------------------------------------------------------------------------
def save_cache(
    path: str,
    embeddings: np.ndarray,
    texts: list[str],
    timestamps: list[datetime],
) -> None:
    """Persist embeddings + metadata to JSON.

    :param path: Output file path.
    :param embeddings: Float32 (N, D) array.
    :param texts: Aligned text strings.
    :param timestamps: Aligned datetime objects.
    """
    data = {
        "embeddings": embeddings.tolist(),
        "texts": texts,
        "timestamps": [ts.isoformat() for ts in timestamps],
    }
    with open(path, "w", encoding="utf-8") as fh:
        json.dump(data, fh)
    console.print(f"[green]Cache saved ({len(texts)} entries) → {path}[/green]")


def load_cache(path: str) -> tuple[np.ndarray, list[str], list[datetime]]:
    """Load embeddings + metadata from JSON cache.

    :param path: Path to cache JSON.
    :return: Tuple of (embeddings, texts, timestamps).
    """
    with open(path, encoding="utf-8") as fh:
        data = json.load(fh)
    embeddings = np.array(data["embeddings"], dtype=np.float32)
    texts = data["texts"]
    timestamps = [datetime.fromisoformat(ts) for ts in data["timestamps"]]
    console.print(f"[dim]Cache loaded ← {path}  ({len(texts)} entries)[/dim]")
    return embeddings, texts, timestamps


# ---------------------------------------------------------------------------
# Intrinsic dimensionality estimators
# ---------------------------------------------------------------------------
def participation_ratio(eigenvalues: np.ndarray) -> float:
    """PR = (Σλ)² / Σλ² — effective number of active dimensions."""
    lam = eigenvalues[eigenvalues > 0]
    return float(lam.sum() ** 2 / (lam**2).sum())


def twonn_id(X: np.ndarray) -> float:
    """TwoNN intrinsic dimensionality estimator (Facco et al. 2017).

    :param X: Data matrix (N, D), L2-normalised.
    :return: Estimated intrinsic dimensionality.
    """
    nbrs = NearestNeighbors(n_neighbors=3, metric="cosine").fit(X)
    distances, _ = nbrs.kneighbors(X)
    r1 = distances[:, 1]
    r2 = distances[:, 2]
    mask = r1 > 0
    mu = r2[mask] / r1[mask]
    return float(len(mu) / np.sum(np.log(mu)))


def elbow_pca(eigenvalues: np.ndarray, threshold: float = 0.90) -> int:
    """Number of PCA components needed to explain *threshold* variance.

    :param eigenvalues: PCA explained variance array.
    :param threshold: Cumulative variance threshold (0–1).
    :return: Number of components.
    """
    cumvar = np.cumsum(eigenvalues) / eigenvalues.sum()
    return int(np.searchsorted(cumvar, threshold)) + 1


# ---------------------------------------------------------------------------
# Retrieval evaluation (temporal ground-truth)
# ---------------------------------------------------------------------------
def build_retrieval_pairs(
    timestamps: list[datetime],
) -> list[tuple[str, list[int]]]:
    """Build (query_text, relevant_indices) pairs from PEPYS_QUERIES.

    A corpus entry is "relevant" if its timestamp falls within the
    (year_start, year_end, month_start, month_end) window.

    :param timestamps: Datetime list aligned with the corpus.
    :return: List of (query_string, [relevant_corpus_indices]).
    """
    pairs = []
    for query_text, yr0, yr1, mo0, mo1 in PEPYS_QUERIES:
        relevant = [
            i
            for i, ts in enumerate(timestamps)
            if (
                yr0 <= ts.year <= yr1
                and (yr0 < ts.year or ts.month >= mo0)
                and (ts.year < yr1 or ts.month <= mo1)
            )
        ]
        if relevant:
            pairs.append((query_text, relevant))
    return pairs


def eval_retrieval(
    E: np.ndarray,
    Q: np.ndarray,
    rel_lists: list[list[int]],
    k: int = 10,
) -> float:
    """MRR@K over pre-embedded query vectors.

    :param E: Corpus embeddings (N, D), L2-normalised.
    :param Q: Query embeddings (M, D), L2-normalised.
    :param rel_lists: Relevant corpus indices for each query.
    :param k: Rank cutoff.
    :return: Mean Reciprocal Rank.
    """
    nbrs = NearestNeighbors(n_neighbors=min(k + 1, len(E)), metric="cosine").fit(E)
    scores = []
    for qvec, rel in zip(Q, rel_lists):
        _, indices = nbrs.kneighbors(qvec.reshape(1, -1))
        retrieved = list(indices[0])[:k]
        rr = 0.0
        for rank, idx in enumerate(retrieved, 1):
            if idx in rel:
                rr = 1.0 / rank
                break
        scores.append(rr)
    return float(np.mean(scores))


