"""
pepys_manifold_explorer.py
--------------------------
Explore the all-mpnet-base-v2 manifold using Samuel Pepys's diary as corpus,
with full ManifoldWalker + ManifoldObserver flight instrumentation.

Uses the complete diverse embeddings generated by diary_kg's DiaryTransformer
ingestion stack (``pepys_diverse_embeddings.json``, 2356 chunks from the
full 1660–1669 corpus).  Results are directly comparable to the diary_kg
``pepys_mpnet_explorer.py`` output — the key addition here is the
ManifoldWalker/ManifoldObserver flight from proteusPy.

Pipeline
--------
1. Load ``pepys_diverse_embeddings.json`` (or rebuild via DiaryTransformer).
2. Intrinsic dimensionality:
     - PCA explained-variance elbow (90 / 95 / 99 %)
     - Participation Ratio: PR = (Σλ)² / Σλ²
     - TwoNN estimator (Facco et al. 2017)
3. MRL truncation quality: MRR@10 at 64 / 128 / 256 / 512 / 768 D using
   Pepys-specific queries tied to known historical events.
4. ManifoldWalker flight: pick the two chunks most distant in cosine space,
   fly origin → destination, observe path height and curvature.
5. Save results JSON + 4-panel dark figure.

Usage
-----
  # full run from cached diverse embeddings
  python benchmarks/pepys_manifold_explorer.py

  # quick test (500-chunk temporal subset)
  python benchmarks/pepys_manifold_explorer.py --n 500

  # rebuild cache from diary_kg (requires diary_kg on sys.path)
  python benchmarks/pepys_manifold_explorer.py --rebuild-cache \\
      --diary ../diary_kg/pepys/pepys_clean.txt

  # skip the ManifoldWalker step
  python benchmarks/pepys_manifold_explorer.py --no-walker

Requirements
------------
  numpy, scikit-learn, rich, matplotlib, sentence-transformers
  proteusPy (ManifoldModel, ManifoldObserver)
  diary_kg (optional — only needed for --rebuild-cache)
"""

